
public class Ifelseif {
	
		   public static void main(String[] args) {   
		      int num = 15;
		      
		      if (num > 0) {
		         System.out.println("POSITIVE NUMBER");
		      }
		      else if (num < 0) {
		         System.out.println("NEGATIVE NUMBER");
		      }
		      else {
		         System.out.println("NUMBER 0");
		      } 
		   }
		}
